res=float(input("What's your age"))
if res<4:
    print("not in school")
elif res>=4 and res<5:
    print("Pre School")
elif res>=5 and res<10:
    print("Lower School")
elif res>=10 and res<13:
    print("Middle School")
elif res>=13 and res<18:
    print("Upper School")
elif res>=18:
    print("not in school")